                + "from veterinario v inner join mascotas m on v.id_v = m.id inner join medicinas med on med.id_medicina = m.id inner join tipo t on m.id = t.id_tipo "
                + "where v.nombre_vet='Wiliam L√≥pez'";
        Statement st;
